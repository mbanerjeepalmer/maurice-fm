	<h1 id="make-better-slides">Make better slides</h1>
	<ul>
		<li>Give your audience a map at the start</li>
		<li>One idea per slide</li>
		<li>
			The heading on each slide should be the point that you want the audience takes away from it
		</li>
		<li>Words on slides are actually fine</li>
		<li>Use visual aids to make your point easy to grasp</li>
	</ul>
	<h2 id="the-peloton-activist-investor-deck-is-a-good-example">
		The Peloton activist investor deck is a good example
	</h2>
	<p>
		An example of this done well is the deck made by Blackwells Capital criticising Peloton:
		https://www.blackwellscap.com/wp-content/uploads/2022/02/BW_Peloton_Presentation_Feb072022.pdf
	</p>
	<ul>
		<li>
			Slide 3 gives the audience a map of the points Blackwells wants to make:
			<ul>
				<li>Peloton Is An Attractive Business</li>
				<li>Peloton has underperformed</li>
				<li>Peloton Has Been Grossly Mismanaged</li>
				<li>Peloton’s Board and Governance Lack Accountability and Alignment</li>
				<li>The Board Should Immediately Put Peloton Up For Sale</li>
			</ul>
		</li>
		<li>
			Slide 24 gives the audience a map of one of the sub-points. They think that ‘PELOTON’S
			UNDERPERFORMANCE IS DUE TO MISMANAGEMENT’ and they’re giving you five reasons to believe this.
		</li>
		<li>
			Slides 25 - 42 each illustrate one idea with a clear point as the headline and visual aides to
			illustrate.
			<ul>
				<li>
					Slide 26: Damning heading ‘LACK OF QUALIFICATION: MR. FOLEY MANAGES WITH UNBRIDLED
					OPTIMISM RATHER THAN DISCIPLINE’.
				</li>
				<li>
					Slide 34: MR. FOLEY’S WIFE RUNS ONE OF THE BUSINESSES and “[I]t’s just… the dynamic is a
					little awkward with Jill and John.” are illustrated by a picture of Jill and John
					together.
				</li>
			</ul>
		</li>
	</ul>
	<h2 id="the-heading-is-the-takeaway">The heading is the takeaway</h2>
	<ul>
		<li>
			Give your audience the takeaway in the heading
			<ul>
				<li>Bad: ‘Strategy’</li>
				<li>Good: ‘Our strategy is reduce production costs through automation’</li>
			</ul>
		</li>
		<li>
			This helps:
			<ul>
				<li>your audience get the point with a glance</li>
				<li>you have something you can read out when you’re presenting</li>
			</ul>
		</li>
	</ul>
	<h2 id="bad-advice-for-slides">Bad advice for slides</h2>
	<p>
		I’ve falled victim to bad advice about slides. Here’s what to avoid: - ‘If someone found a
		print-out of your slides on the train they shouldn’t make sense on their own’: This advice
		sounds clever but actually ruins your productivity. To increase the chance of a successful
		presentation So your slides should stand on their own. The Blackwells slides wouldn’t have gone
		viral if they didn’t. - ‘Talk like TED’: At TED the audience pays to see entertaining and
		interesting people speak. Sure, take some inspiration. But in a work context you’re pretty much
		the opposite. - ‘Words on slides are bad’: This is a sliding scale.
	</p>
